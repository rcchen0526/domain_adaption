class Discriminator(nn.Module):
    def __init__(self):
        super(Discriminator, self).__init__()
    
        self.h0 = nn.Sequential(
            nn.Conv2d(3, 64*2, 5, stride=2, padding=1),
            nn.InstanceNorm2d(64*2),
            nn.LeakyReLU(0.2, inplace=True)
        )
        self.h0_pred = nn.Sequential(
            nn.Conv2d(64*2, 1, 5, stride=1, padding=1)
        )
        self.h1 = nn.Sequential(
            nn.Conv2d(64*2, 64*2, 5, stride=1, padding=1),
            nn.InstanceNorm2d(64*2),
            nn.LeakyReLU(0.2, inplace=True)
        )
        self.h1_pred = nn.Sequential(
            nn.Conv2d(64*2, 1, 10, stride=1, padding=1)
        )
        self.h2 = nn.Sequential(
            nn.Conv2d(64*2, 64*4, 5, stride=1, padding=1),
            nn.InstanceNorm2d(64*4),
            nn.LeakyReLU(0.2, inplace=True)
        )
        self.h3 = nn.Sequential(
            nn.Conv2d(64*4, 64*8, 5, stride=1, padding=1),
            nn.InstanceNorm2d(64*8),
            nn.LeakyReLU(0.2, inplace=True)
        )
        self.h3_pred = nn.Sequential(
            nn.Conv2d(64*8, 1, 10, stride=1, padding=1)
        )
        self.h4 = nn.Sequential(
            nn.Conv2d(64*8, 64*8, 5, stride=1, padding=1),
            nn.InstanceNorm2d(64*8),
            nn.LeakyReLU(0.2, inplace=True)
        )
        self.h5 = nn.Sequential(
            nn.Conv2d(64*8, 64*16, 5, stride=1, padding=1),
            nn.InstanceNorm2d(64*16),
            nn.LeakyReLU(0.2, inplace=True)
        )
        self.h5_pred = nn.Sequential(
            nn.Conv2d(64*16, 1, 6, stride=1, padding=1)
        )
        self.h6 = nn.Sequential(
            nn.Conv2d(64*16, 64*16, 5, stride=1, padding=1),
            nn.InstanceNorm2d(64*16),
            nn.LeakyReLU(0.2, inplace=True)
        )
        self.h6_pred = nn.Sequential(
            nn.Conv2d(64*16, 1, 3, stride=1, padding=1)
        )

        self.reset_params()

    @staticmethod
    def weight_init(m):
        if isinstance(m, nn.Conv2d):
            init.xavier_normal_(m.weight)
            init.constant_(m.bias, 0)


    def reset_params(self):
        for i, m in enumerate(self.modules()):
            self.weight_init(m)

    def forward(self, img):
        img = self.h0(img)
        h0_pred = self.h0_pred(img)
        img = self.h1(img)
        h1_pred = self.h1_pred(img)
        img = self.h3(self.h2(img))
        h3_pred = self.h3_pred(img)
        img = self.h5(self.h4(img))
        h5_pred = self.h5_pred(img)
        h6_pred = self.h6_pred(self.h6(img))
        return h0_pred, h1_pred, h3_pred, h5_pred, h6_pred

class Net(nn.Module):
    def __init__(self, encoder, decoder):
        super(Net, self).__init__()
        enc_layers = list(encoder.children())
        self.enc_1 = nn.Sequential(*enc_layers[:4])  # input -> relu1_1
        self.enc_2 = nn.Sequential(*enc_layers[4:11])  # relu1_1 -> relu2_1
        self.enc_3 = nn.Sequential(*enc_layers[11:18])  # relu2_1 -> relu3_1
        self.enc_4 = nn.Sequential(*enc_layers[18:31])  # relu3_1 -> relu4_1
        self.decoder = decoder
        self.mse_loss = nn.MSELoss()

        # fix the encoder
        for name in ['enc_1', 'enc_2', 'enc_3', 'enc_4']:
            for param in getattr(self, name).parameters():
                param.requires_grad = False

    # extract relu1_1, relu2_1, relu3_1, relu4_1 from input image
    def encode_with_intermediate(self, input):
        results = [input]
        for i in range(4):
            func = getattr(self, 'enc_{:d}'.format(i + 1))
            results.append(func(results[-1]))
        return results[1:]

    # extract relu4_1 from input image
    def encode(self, input):
        for i in range(4):
            input = getattr(self, 'enc_{:d}'.format(i + 1))(input)
        return input

    def calc_content_loss(self, input, target):
        assert (input.size() == target.size())
        assert (target.requires_grad is False)
        return self.mse_loss(input, target)

    def calc_style_loss(self, input, target):
        assert (input.size() == target.size())
        assert (target.requires_grad is False)
        input_mean, input_std = calc_mean_std(input)
        target_mean, target_std = calc_mean_std(target)
        return self.mse_loss(input_mean, target_mean) + \
               self.mse_loss(input_std, target_std)

    def forward(self, content, style, alpha=1.0):
        assert 0 <= alpha <= 1
        style_feats = self.encode_with_intermediate(style)
        content_feat = self.encode(content)
        t = adain(content_feat, style_feats[-1])
        t = alpha * t + (1 - alpha) * content_feat

        g_t = self.decoder(t)
        g_t_feats = self.encode_with_intermediate(g_t)

        loss_c = self.calc_content_loss(g_t_feats[-1], t)
        loss_s = self.calc_style_loss(g_t_feats[0], style_feats[0])
        for i in range(1, 4):
            loss_s += self.calc_style_loss(g_t_feats[i], style_feats[i])
        return loss_c, loss_s
